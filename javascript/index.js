var a;
a = 30;

// Number
console.log(a, typeof a);

// String
a = "Javascript is Awesome";
console.log(a, typeof a);

// Boolean
a = true;
console.log(a, typeof a);

a = undefined;
console.log(a, typeof a);

a = null;
console.log(a, typeof a);

// Number

var b = 10;
console.log(b); // Integer

b = 10.111;
console.log(b); // Float

b = 123.1234567891;
console.log(b); // Double

b = 0x23ab;
console.log(b);  // Hexa Decimal

b = 0O123;
console.log(b);  // Octal

b = true;
console.log(b); // Boolean

b = false;
console.log(b); // Boolean








